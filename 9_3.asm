			LIST P=PIC16F84A
			#INCLUDE<P16F84A.INC>
		
			__CONFIG _HS_OSC&_CP_OFF&_WDT_OFF&_PWRTE_ON

W_TEMP		EQU		0CH
STATUS_TEMP	EQU		0DH
CNT1		EQU		0EH
CNTB		EQU		0FH
TMPA		EQU		010H
OUT			EQU		011H

			ORG		0H
			GOTO	MAIN
			
			ORG		04H

;PUSH
			MOVWF	W_TEMP
			MOVF	STATUS,W
			MOVWF	STATUS_TEMP

			CALL	CRECV
			MOVWF	OUT

;POP
			MOVF	STATUS_TEMP,W
			MOVWF	STATUS
			SWAPF	W_TEMP,F
			SWAPF	W_TEMP,W
			BCF		INTCON,INTF
			RETFIE

;REMAINING CODE GOES HERE

MAIN
			BSF		STATUS,RP0
			BCF		INTCON,GIE
			CLRF	TRISA
			MOVLW	0FH
			MOVWF	TRISB
			BCF		OPTION_REG,INTEDG
			BCF		STATUS,RP0
			BSF		INTCON,GIE
			BSF		INTCON,INTE

MAINLP
			MOVF	OUT,W
			MOVWF	PORTA
			ANDLW	0F0H
			MOVWF	PORTB
			GOTO	MAINLP


CRECV
			CLRF	TMPA
			MOVLW	08H
			MOVWF	CNTB
			CALL	TIME156

CRECVLP
			BTFSC	PORTB,0
			BSF		STATUS,C
			BTFSS	PORTB,0
			BCF		STATUS,C
			RRF		TMPA,F
			CALL	TIME104
			DECFSZ	CNTB,F
			GOTO	CRECVLP
			MOVF	TMPA,W
			RETURN

TIME156
			MOVLW	0C3H
			MOVWF	CNT1
LOOP1
			NOP
			DECFSZ	CNT1,F
			GOTO	LOOP1
			RETURN

TIME104
			MOVLW	082H
			MOVWF	CNT1
LOOP2
			NOP
			DECFSZ	CNT1,F
			GOTO	LOOP2
			RETURN

			END
